#!/bin/bash
set -euo pipefail

# claude-code-kit installer
# Usage: bash install.sh [target-project-path]

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
TARGET="${1:-.}"
TARGET=$(cd "$TARGET" && pwd)

echo "=== claude-code-kit installer ==="
echo "Target: $TARGET"
echo ""

# --- Verify git repo ---
if ! git -C "$TARGET" rev-parse --git-dir >/dev/null 2>&1; then
  echo "ERROR: $TARGET is not a git repository."
  exit 1
fi

# --- Select skills ---
AVAILABLE_SKILLS=("worktree-enter" "worktree-exit" "code-review")
SELECTED_SKILLS=()

echo "Available skills:"
for i in "${!AVAILABLE_SKILLS[@]}"; do
  echo "  $((i+1)). ${AVAILABLE_SKILLS[$i]}"
done
echo "  a. All"
echo ""
read -rp "Install which skills? (e.g. 1,2 or a for all): " choice

if [[ "$choice" == "a" || "$choice" == "A" ]]; then
  SELECTED_SKILLS=("${AVAILABLE_SKILLS[@]}")
else
  IFS=',' read -ra indices <<< "$choice"
  for idx in "${indices[@]}"; do
    idx=$(echo "$idx" | tr -d ' ')
    if [[ "$idx" =~ ^[0-9]+$ ]] && [ "$idx" -ge 1 ] && [ "$idx" -le "${#AVAILABLE_SKILLS[@]}" ]; then
      SELECTED_SKILLS+=("${AVAILABLE_SKILLS[$((idx-1))]}")
    fi
  done
fi

if [ ${#SELECTED_SKILLS[@]} -eq 0 ]; then
  echo "No skills selected. Exiting."
  exit 0
fi

echo ""
echo "Installing: ${SELECTED_SKILLS[*]}"

# --- Install skills ---
COMMANDS_DIR="$TARGET/.claude/commands"
mkdir -p "$COMMANDS_DIR"

for skill in "${SELECTED_SKILLS[@]}"; do
  cp "$SCRIPT_DIR/skills/$skill.md" "$COMMANDS_DIR/$skill.md"
  echo "[ok] $skill"
done

# --- Worktree setup (only if worktree skills selected) ---
HAS_WORKTREE=false
for skill in "${SELECTED_SKILLS[@]}"; do
  if [[ "$skill" == "worktree-enter" || "$skill" == "worktree-exit" ]]; then
    HAS_WORKTREE=true
    break
  fi
done

if [ "$HAS_WORKTREE" = true ]; then
  echo ""
  echo "--- Worktree symlink config ---"
  echo "When a worktree is created, shared files can be symlinked from the main repo."
  echo "Enter file/directory paths to symlink (e.g. .env, config/, .npmrc)"
  echo "(Empty to finish. No input = .env only)"
  echo ""

  SYMLINKS=()
  while true; do
    read -rp "Symlink target (Enter to finish): " item
    if [ -z "$item" ]; then
      break
    fi
    SYMLINKS+=("$item")
  done

  if [ ${#SYMLINKS[@]} -eq 0 ]; then
    SYMLINKS=(".env")
    echo "Default: .env"
  fi

  # Generate setup-worktree.sh
  SCRIPTS_DIR="$TARGET/scripts"
  mkdir -p "$SCRIPTS_DIR"

  {
    cat << 'HEAD'
#!/bin/bash
# Worktree symlink setup — generated by claude-code-kit
# Usage:
#   ./scripts/setup-worktree.sh                    # current directory
#   ./scripts/setup-worktree.sh /path/to/worktree  # explicit path

WORKTREE_ROOT="${1:-$(pwd)}"
MAIN_REPO="${CONDUCTOR_ROOT_PATH:-}"

if [ -z "$MAIN_REPO" ]; then
  if ! git -C "$WORKTREE_ROOT" rev-parse --git-dir >/dev/null 2>&1; then
    echo "Not a git repo — skipping setup"
    exit 0
  fi
  MAIN_REPO=$(git -C "$WORKTREE_ROOT" worktree list 2>/dev/null | awk 'NR==1{print $1}')
  if [ -z "$MAIN_REPO" ]; then
    echo "Not a worktree — skipping setup"
    exit 0
  fi
fi

WORKTREE_REAL=$(cd "$WORKTREE_ROOT" 2>/dev/null && pwd) || exit 0
MAIN_REAL=$(cd "$MAIN_REPO" 2>/dev/null && pwd) || exit 0

if [ "$MAIN_REAL" = "$WORKTREE_REAL" ]; then
  echo "Running in main repo — no setup needed"
  exit 0
fi

echo "Main repo:  $MAIN_REPO"
echo "Worktree:   $WORKTREE_ROOT"

HEAD

    for item in "${SYMLINKS[@]}"; do
      cat << ITEM
# $item
if [ -e "\$MAIN_REPO/$item" ] && [ ! -e "\$WORKTREE_ROOT/$item" ]; then
  ln -sf "\$MAIN_REPO/$item" "\$WORKTREE_ROOT/$item"
  echo "$item -> \$MAIN_REPO/$item"
fi

ITEM
    done

    echo 'echo "Worktree setup complete"'
    echo 'exit 0'
  } > "$SCRIPTS_DIR/setup-worktree.sh"

  chmod +x "$SCRIPTS_DIR/setup-worktree.sh"
  echo "[ok] setup-worktree.sh (symlinks: ${SYMLINKS[*]})"

  # --- .gitignore check ---
  GITIGNORE="$TARGET/.gitignore"
  for item in "${SYMLINKS[@]}"; do
    if [ ! -f "$GITIGNORE" ] || ! grep -qxF "$item" "$GITIGNORE" 2>/dev/null; then
      read -rp "Add '$item' to .gitignore? (y/N): " yn
      if [[ "$yn" =~ ^[Yy]$ ]]; then
        echo "$item" >> "$GITIGNORE"
        echo "  -> added to .gitignore"
      fi
    fi
  done
fi

echo ""
echo "=== Done ==="
echo ""
echo "Usage in Claude Code:"
for skill in "${SELECTED_SKILLS[@]}"; do
  echo "  /$skill"
done
if [ "$HAS_WORKTREE" = true ]; then
  echo ""
  echo "Merge rule: git merge --no-ff feat/<task> -m \"merge: feat/<task> -> main\""
fi
